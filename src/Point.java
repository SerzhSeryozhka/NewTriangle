import java.util.Scanner;

public class Point{
    private double x, y;

    public Point() {
        this.x=x;
        this.y=y;
    }

    public double getX() {
        return x;
    }

    public void setX(int x) {
        this.x =x;
    }

    public double getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
